let EDIT_PRODUCT = 'EDIT_PRODUCT'
let UPDATE_PRODUCT = 'UPDATE_PRODUCT'

export const productsReducer = (state = products, action) => {
    switch (action.type) {
        case EDIT_PRODUCT:
            return {...state, selectedProduct: action.payload}
        case UPDATE_PRODUCT:
            let arr = [...state];
            let idx = null

            arr.forEach((card, i) => {
                if (card.id === +action.payload.id) {
                    idx = i
                }
            })
            
            arr[idx].title = action.payload.title
            arr[idx].price = action.payload.price

           return arr
        default: return state
    }
}
